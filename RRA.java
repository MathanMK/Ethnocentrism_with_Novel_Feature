public class RRA {
    private int row;
    private int col;
    private int radius;

    public RRA(int row, int col, int radius) {
        this.row = row;
        this.col = col;
        this.radius = radius;
    }

    public int getRow() {
        return row;
    }

    public int getCol() {
        return col;
    }

    public int getRadius() {
        return radius;
    }
}